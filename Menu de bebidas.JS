// Solicita a opção do usuário
let opcao = 1;

// Avalia a opção
switch (opcao) {
  case 1:
    console.log("Você escolheu Água");
    break;
  case 2:
    console.log("Você escolheu Refrigerante");
    break;
  case 3:
    console.log("Você escolheu Suco");
    break;
  case 4:
    console.log("Você escolheu Café");
    break;
  default:
    console.log("Opção inválida!");
}